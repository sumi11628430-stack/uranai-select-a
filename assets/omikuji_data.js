function drawOmikuji() {
  var total = OMIKUJI.reduce(function (a, b) { return a + b.weight; }, 0);
  var r = Math.random() * total;
  for (var i = 0; i < OMIKUJI.length; i++) {
    r -= OMIKUJI[i].weight;
    if (r <= 0) return OMIKUJI[i];
  }
  return OMIKUJI[OMIKUJI.length - 1];
}
