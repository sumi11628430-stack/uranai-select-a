/* ---- 1文字の画数を求める（対応可否も返す） ------------------------ */
function charStrokes(ch) {
  if (KYUJITAI[ch] !== undefined) return { ok: true, n: KYUJITAI[ch] };
  if (KYUJITAI_SELF[ch] !== undefined) return { ok: true, n: KYUJITAI_SELF[ch] };
  if (COMMON_KANJI[ch] !== undefined) return { ok: true, n: COMMON_KANJI[ch] };

  if (DAKUTEN_KATA[ch] !== undefined) {
    var baseD = KANA_BASE[DAKUTEN_KATA[ch]];
    return baseD !== undefined ? { ok: true, n: baseD + 2 } : { ok: false, n: 0 };
  }
  if (HANDAKUTEN_KATA[ch] !== undefined) {
    var baseH = KANA_BASE[HANDAKUTEN_KATA[ch]];
    return baseH !== undefined ? { ok: true, n: baseH + 1 } : { ok: false, n: 0 };
  }
  if (KANA_BASE[ch] !== undefined) return { ok: true, n: KANA_BASE[ch] };

  var kata = HIRA_TO_KATA[ch];
  if (kata !== undefined) {
    if (kata.length === 2) {
      // 濁点・半濁点付きひらがな（例: が → カ゛）
      var mark = kata.charAt(1);
      var baseChar = kata.charAt(0);
      var b = KANA_BASE[baseChar];
      if (b === undefined) return { ok: false, n: 0 };
      return { ok: true, n: b + (mark === "゛" ? 2 : 1) };
    }
    var kb = KANA_BASE[kata];
    return kb !== undefined ? { ok: true, n: kb } : { ok: false, n: 0 };
  }

  return { ok: false, n: 0 };
}
