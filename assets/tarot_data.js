function suitCardName(suitKey, rank) {
  var suit = TAROT_SUITS[suitKey].name;
  if (rank === "ace") return suit + "のエース";
  if (rank === "page") return suit + "のペイジ";
  if (rank === "knight") return suit + "のナイト";
  if (rank === "queen") return suit + "のクイーン";
  if (rank === "king") return suit + "のキング";
  return suit + "の" + rank;
}

