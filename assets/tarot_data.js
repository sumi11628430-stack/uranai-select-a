function astroChip(card) {
  if (!card.astro) return null;
  var a = card.astro;
  if (a.type === "element") return a.label;
  if (a.type === "planet") return a.label;
  if (a.type === "sign") return a.label;
  if (a.type === "decan") return a.sign + "（第" + a.n + "デカン）・" + a.planet;
  return null;
}

function drawCelticCross() {
  var pool = ALL_CARDS.slice();
  var drawn = [];
  for (var i = 0; i < 10; i++) {
    var idx = Math.floor(Math.random() * pool.length);
    var card = pool.splice(idx, 1)[0];
    drawn.push({ card: card, reversed: Math.random() < 0.5, position: CELTIC_POSITIONS[i] });
  }
  return drawn;
}
